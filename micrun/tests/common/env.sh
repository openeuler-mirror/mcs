    echo "  K3S_EDGE_CONTAINERD_MODE=$K3S_EDGE_CONTAINERD_MODE"
    echo "  K3S_EDGE_CONTAINERD_ADDR=$K3S_EDGE_CONTAINERD_ADDR"
    echo "  K3S_LOCAL_USE_REVERSE_TUNNEL=$K3S_LOCAL_USE_REVERSE_TUNNEL"
    echo "  K3S_INTERACTION_MODE=$K3S_INTERACTION_MODE"
    echo "  K3S_INTERACTION_POD=$K3S_NAMESPACE/$K3S_INTERACTION_POD_NAME"
    echo "  K3S_INTERACTION_EXPECT=$K3S_INTERACTION_EXPECT"
    echo "  LOG_DIR=$TEST_LOG_DIR"
    echo ""
}

