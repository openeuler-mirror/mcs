    echo "  REMOTE_PORT=${TEST_REMOTE_PORT:-<default>}"
    echo "  REMOTE_PASSWORD=${TEST_REMOTE_PASSWORD:+<set>}"
    echo "  TEST_IMAGE=$TEST_IMAGE"
    echo "  K3S_SOURCE_IMAGE_REF=$K3S_SOURCE_IMAGE_REF"
